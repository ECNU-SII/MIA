def run_evaluator(evaluator_prompt_template: str, question: str, final_output: str, trajectory = "") -> str:
    if trajectory:
        prompt = evaluator_prompt_template.format(
            question=question,
            trajectory=trajectory,
            final_output=final_output
        )
    else:
        prompt = evaluator_prompt_template.format(
            question=question,
            final_output=final_output
        )
    return call_llm(prompt, temperature=0.1)

def call_llm(prompt_text: str, temperature: float = 0.2) -> str:
    try:
        response = judge_client.chat.completions.create(
            model=model_name,
            messages=[{"role": "user", "content": prompt_text}],
            temperature=temperature,
            max_tokens=1000,
            extra_body={"chat_template_kwargs": {"enable_thinking": False}}
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        print(f"LLM Call Error: {e}")
        return "Error"

def evaluate_agent_without_gt(question: str, trajectory: str, final_output: str) -> dict:
    eval_1_result = run_evaluator(
        PROMPT_EVALUATOR_1, 
        question=question, trajectory=trajectory, final_output=final_output
    )
    eval_2_result = run_evaluator(
        PROMPT_EVALUATOR_2, 
        question=question, trajectory=trajectory, final_output=final_output
    )
    eval_3_result = run_evaluator(
        PROMPT_EVALUATOR_3, 
        question=question, final_output=final_output
    )
    ac_prompt = PROMPT_FINAL_ARBITER.format(
        question=question,
        trajectory=trajectory,
        final_output=final_output,
        evaluator_1_result=eval_1_result,
        evaluator_2_result=eval_2_result,
        evaluator_3_result=eval_3_result
    )
    ac_result = call_llm(ac_prompt, temperature=0.0)
    final_verdict_char = ac_result.replace(".", "").replace(" ", "").strip().upper()
    is_correct = "correct" if "A" in final_verdict_char else "incorrect"
    return {
        "is_correct": is_correct,
        "raw_verdict": final_verdict_char,
        "evaluator_1_feedback": eval_1_result,
        "evaluator_2_feedback": eval_2_result,
        "evaluator_3_feedback": eval_3_result
    }

